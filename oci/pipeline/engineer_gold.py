def read_silver(spark, silver_bucket, namespace, table_name):
    uri = f"oci://{silver_bucket}@{namespace}/rawdata/{table_name}/"
    if SILVER_READ_FORMAT == "delta":
        return spark.read.format("delta").load(uri)
    return spark.read.parquet(uri)

def read_dim_csv(spark, landing_bucket, namespace, subfolder, filename):
    uri = f"oci://{landing_bucket}@{namespace}/{subfolder}/{filename}"
    return spark.read.csv(uri, header=True, inferSchema=True)

def drop_internal_cols(df):
    internal = ["_execution_id", "_data_inclusao", "_data_alteracao_silver"]
    for c in internal:
        if c in df.columns:
            df = df.drop(c)
    return df

def get_select_cols(df, alias, exclude, prefix=None):
    """Generate column list with optional prefix renaming (Fabric consolidation pattern)."""
    exclude_lower = {c.lower() for c in exclude}
    cols = []
    for c in df.columns:
        if c.lower() not in exclude_lower:
            if prefix:
                cols.append(f"{alias}.{c} AS {prefix}_{c}")
            else:
                cols.append(f"{alias}.{c}")
    return cols

def build_book_for_safras(spark, sql_template, safras):
    """Execute book SQL template for each SAFRA, return unioned DataFrame."""
    dfs = []
    for safra in safras:
        cutoff = safra_to_cutoff(safra)
        query = sql_template.format(safra=safra, data_cutoff=cutoff)
        df = spark.sql(query)
        dfs.append(df)
        print(f"  SAFRA {safra}: query executed")
    if not dfs:
        return None
    result = dfs[0]
    for df in dfs[1:]:
        result = result.unionByName(df, allowMissingColumns=True)
    return result


